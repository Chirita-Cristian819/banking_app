    public void setScore(String Score){
        finalScore = Double.parseDouble(Score);
        if (finalScore > 100) finalScore = 100;
        
        score.setText(String.valueOf(finalScore));
        backgroundColor();
    }    
    
    private void backgroundColor(){
        if (finalScore < 50) {
            panel.setBackground(Color.BLACK);
            risk.setForeground(Color.WHITE);
            label.setForeground(Color.WHITE);
            score.setForeground(Color.WHITE);
            risk.setText("Massive risk");
            
        } else if (finalScore >= 50 && finalScore < 60) {
            panel.setBackground(new Color(228, 0, 60));
            risk.setText("Very high risk");
            
        } else if (finalScore >= 60 && finalScore < 70) {
            panel.setBackground(new Color(255, 51, 0));
            risk.setText("Considerable risk");
            
        } else if (finalScore >= 70 && finalScore < 80) {
            panel.setBackground(new Color(235, 235, 0));
            risk.setText("Moderate risk");
            
        } else if (finalScore >= 80 && finalScore < 90) {
            panel.setBackground(new Color(51, 255, 51));
            risk.setText("Low risk");
            
        } else if (finalScore >= 90) {
            panel.setBackground(new Color(51, 204, 0));
            risk.setText("Negligable risk");
        }
            
    }
     
    private void errorWindow(String message){
        JLabel windowLabel = new JLabel(message);
        windowLabel.setFont(new java.awt.Font("Times New Roman", java.awt.Font.PLAIN, 17));
        JOptionPane.showMessageDialog(null, windowLabel, "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    private void confirmationWindow(String message){
        JLabel windowLabel = new JLabel(message);
        windowLabel.setFont(new java.awt.Font("Times New Roman", java.awt.Font.PLAIN, 17));
        JOptionPane.showMessageDialog(null, windowLabel, "Success", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void addCredit() {
        
        double monthlyRate = applicationForm.getLoanValue() / applicationForm.getLoanDuration();
        
        int reply = JOptionPane.showConfirmDialog(null, "Grant Loan?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            try {
                handler.AddCredit(applicationForm.getLoanValue(), monthlyRate, 
                        applicationForm.getLoanDuration(), applicationForm.getEmployeeEmail(), applicationForm.getClientEmail());               
            } catch (IOException | JSONException ex) {
            }
            confirmationWindow("Loan granted successfully!");
            this.setVisible(false);
            this.setLocationRelativeTo(null);
            applicationForm.reinitialize();
        }
    }
        
    
