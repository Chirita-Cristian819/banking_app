    private void grantLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grantLoanActionPerformed
       
        switch (employeeLevel) {
            case 1:
                if (applicationForm.getLoanValue() > 1000) {
                    errorWindow("You don't have the permission to grant loans exceeding 1000 euros!");
                } else {
                    addCredit();
                }
                break;
            case 2:
                if (applicationForm.getLoanValue() > 2500) {
                    errorWindow("You don't have the permission to grant loans exceeding 2500 euros!");
                } else {
                    addCredit();
                }
                break;
            default:
                addCredit();
                break;
        }
        
    }//GEN-LAST:event_grantLoanActionPerformed

